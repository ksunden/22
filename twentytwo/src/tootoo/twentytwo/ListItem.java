package tootoo.twentytwo;

public class ListItem{
    
    public int image;
    public String title;
    public String desc;
    
    public ListItem()
    {
        super();
    }
    
    public ListItem(int orgImage, String orgTitle, String orgDesc)
    {
        image = orgImage;
        title = orgTitle;
        desc = orgDesc;
    }
    
}
